/*Ejercicio 2: if...else
Declara una variable nota y muestra "Aprobado" si es mayor o igual a 6, y "Desaprobado" en caso contrario.*/

let nota = prompt('Ingrese la nota del alumno:');
if (nota >="6"){
    console.log('El alumno/a esta aprobado/a');
}else{
    console.log('El alumno/a esta desaprobado/a');
}